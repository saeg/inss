package br.usp.each.opal.requirement;

public class RequirementDeterminationException extends RuntimeException {

	private static final long serialVersionUID = -5633920983013325931L;

	public RequirementDeterminationException() {
		super();
	}

	public RequirementDeterminationException(String message, Throwable cause) {
		super(message, cause);
	}

	public RequirementDeterminationException(String message) {
		super(message);
	}

	public RequirementDeterminationException(Throwable cause) {
		super(cause);
	}

}
