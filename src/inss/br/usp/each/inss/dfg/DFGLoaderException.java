package br.usp.each.inss.dfg;

public class DFGLoaderException extends Exception {

	private static final long serialVersionUID = -2736504875576131158L;

	public DFGLoaderException() {
		super();
	}

	public DFGLoaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public DFGLoaderException(String message) {
		super(message);
	}

	public DFGLoaderException(Throwable cause) {
		super(cause);
	}

}
